package neu.edu.csye6200.model;

/**
 *
 * @author hasee
 */
public abstract class AbstractPerson {
    private String name;
    private int age;
    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "AbstractPerson{" + "name=" + name + ", age=" + age + ", id=" + id + '}';
    }
    
}
